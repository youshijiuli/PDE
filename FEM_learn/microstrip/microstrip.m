set(ax,'YTickMode','auto');

% Geometry description:
pderect([-1 1 1 0],'R1');
pderect([-1 1 0 -0.20000000000000001],'R2');
pderect([-0.25 0.25 0.10000000000000001 0],'R3');
set(findobj(get(pde_fig,'Children'),'Tag','PDEEval'),'String','R1-R3+R2')

% Boundary conditions:
pdetool('changemode',0)
pdesetbd(11,...
'dir',...
1,...
'1',...
'1')
pdesetbd(9,...
'dir',...
